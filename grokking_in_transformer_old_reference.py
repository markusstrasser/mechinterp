@torch.no_grad()
def analyze_feature_ablation(sae, model, layer, device, p, feature_to_ablate, a, b):
    """
    Demonstrates the causal effect of ablating a single feature.
    """
    print(f"\n--- Causal Ablation Analysis for Feature {feature_to_ablate} ---")
    sae.eval().to(device)
    model.eval().to(device)

    # 1. Baseline run
    test_input = torch.tensor([[a, b, p]], device=device)
    baseline_logits = model(test_input)
    baseline_pred = baseline_logits.argmax(-1).item()
    print(f"Input: ({a} + {b}) % {p} = {(a+b)%p}")
    print(f"Baseline model prediction: {baseline_pred}")

    # 2. Ablation run
    # Get the feature activation value for this input
    _, cache = model.run_with_cache(test_input)
    activation_at_pos = cache[f"blocks.{layer}.hook_resid_post"][:, -1, :]
    c = sae.encode(activation_at_pos)

    # The vector we need to subtract is the feature's contribution
    feature_vector = sae.W_dec_normed[feature_to_ablate]
    feature_contribution = c[:, feature_to_ablate].unsqueeze(1) * feature_vector

    # Define the hook to subtract this vector from the residual stream
    def ablation_hook(resid_post, hook):
        resid_post[:, -1, :] -= feature_contribution
        return resid_post

    ablated_logits = model.run_with_hooks(
        test_input,
        fwd_hooks=[(f"blocks.{layer}.hook_resid_post", ablation_hook)]
    )
    ablated_pred = ablated_logits.argmax(-1).item()
    print(f"Prediction after ablating feature {feature_to_ablate}: {ablated_pred}")
    print(f"Effect: {'SUCCESS' if ablated_pred != baseline_pred else 'NO CHANGE'}")


