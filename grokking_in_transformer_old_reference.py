# --- 1. Minimal SAE Definition (with helper methods) ---
class SparseAutoencoder(nn.Module):
    def __init__(self, d_model, d_sae):
        super().__init__()
        self.d_model = d_model
        self.d_sae = d_sae
        self.W_enc = nn.Parameter(torch.nn.init.kaiming_uniform_(torch.empty(d_model, d_sae)))
        self.b_enc = nn.Parameter(torch.zeros(d_sae))
        self.b_dec = nn.Parameter(torch.zeros(d_model))

    @property
    def W_dec(self):
        return self.W_enc.t()

    @property
    def W_dec_normed(self):
        return self.W_dec / (torch.norm(self.W_dec, p=2, dim=1, keepdim=True) + 1e-8)

    def encode(self, x):
        return F.relu(x @ self.W_enc + self.b_enc)

    def decode(self, c):
        return c @ self.W_dec_normed + self.b_dec

    def forward(self, x):
        c = self.encode(x)
        x_hat = self.decode(c)
        return x_hat, c

# --- 2. Activation Harvesting ---
@torch.no_grad()
def get_activations(model, data, layer, device, batch_size):
    model.eval()
    activations = []
    print("Harvesting activations...")
    for i in tqdm(range(0, len(data), batch_size)):
        batch = data[i:i+batch_size].to(device)
        if batch.shape[0] == 0: continue
        _, cache = model.run_with_cache(batch)
        res_stream_act = cache[f"blocks.{layer}.hook_resid_post"][:, -1, :]
        activations.append(res_stream_act.cpu())
    return torch.cat(activations, dim=0)


